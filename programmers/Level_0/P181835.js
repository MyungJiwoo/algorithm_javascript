// P181835: 조건에 맞게 수열 변환하기 3
function solution(arr, k) {
  return k % 2 == 0 ? arr.map((num) => num + k) : arr.map((num) => num * k);
}

console.log(solution([1, 2, 3, 100, 99, 98], 3));

